#!/bin/sh

#-------------------
# Get current mode:
#-------------------

declare -A hash
current=`xrandr | grep '*' | awk '{print $1}'`

case $1 in

    #----------
    # Zoom In:
    #----------

    'in')
    hash[1600x900_60.00]='1368x768_60.00'
    hash[1368x768_60.00]='1280x720_60.00'
    hash[1280x720_60.00]='1024x576_60.00'
    hash[1024x576_60.00]='960x540_60.00'
    hash[960x540_60.00]='856x480_60.00'
    hash[856x480_60.00]='640x360_60.00'
    hash[640x360_60.00]='1600x900_60.00';;

    #-----------
    # Zoom Out:
    #-----------

    'out')
    hash[1600x900_60.00]='640x360_60.00'
    hash[1368x768_60.00]='1600x900_60.00'
    hash[1280x720_60.00]='1368x768_60.00'
    hash[1024x576_60.00]='1280x720_60.00'
    hash[960x540_60.00]='1024x576_60.00'
    hash[856x480_60.00]='960x540_60.00'
    hash[640x360_60.00]='856x480_60.00';;

    #----------
    # Zoom To:
    #----------

    1)  hash[$current]='1600x900_60.00';;
    2)  hash[$current]='1368x768_60.00';;
    3)  hash[$current]='1280x720_60.00';;
    4)  hash[$current]='1024x576_60.00';;
    5)  hash[$current]='960x540_60.00';;
    6)  hash[$current]='856x480_60.00';;
    7)  hash[$current]='640x360_60.00';;

esac

#--------------
# Switch mode:
#--------------

xrandr --output LVDS1 --mode ${hash[$current]} --panning 1600x900
